let num = 10;

if (num > 10) {
  console.log("Greater than 10");
} else if (num < 10) {
  console.log("Less than 10");
} else {
  console.log("Equal to 10");
}

// Switch Case
let day = "Monday";
switch (day) {
  case "Monday":
    console.log("Start of the week");
    break;
  case "Friday":
    console.log("Weekend is coming!");
    break;
  default:
    console.log("Regular day");
}
